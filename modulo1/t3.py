# ¿Qué es Python?
    # Python es un lenguaje de programación de alto nivel, interpretado, 
    # orientado a objetos y de uso generalizado con semántica dinámica, 
    # que se utiliza para la programación de propósito general.

    # Aunque puede que conozcas a la pitón como una gran serpiente, 
    # el nombre del lenguaje de programación Python proviene de una 
    # vieja serie de comedia de la BBC llamada Monty Python's Flying Circus.

    # En el apogeo de su éxito, el equipo de Monty Python estaba 
    # realizando sus escenas en vivo para audiencias en todo el mundo,
    # incluso en el Hollywood Bowl.

    # Dado que Monty Python es considerado uno de los dos nutrientes 
    # fundamentales para un programador (el otro es la pizza), el creador 
    # de Python nombró el lenguaje en honor al programa de televisión.
    
    
    
    
    # Un proyecto de programación por pasatiempo
            # Las circunstancias en las que se creó Python son un 
            # poco desconcertantes. Según Guido van Rossum:

            # En Diciembre de 1989, estaba buscando un proyecto de programación 
            # de "pasatiempo" que me mantendría ocupado durante la semana de Navidad.
            # Mi oficina (...) estaría cerrada, pero tenía una computadora en casa y no 
            # mucho más en mis manos. Decidí escribir un intérprete para el nuevo lenguaje 
            # de scripting en el que había estado pensando últimamente: un descendiente de 
            # ABC que atraería a los hackers de Unix/C.Elegí Python como el título de 
            # trabajo para el proyecto, estando en un estado de ánimo ligeramente irreverente 
            # (y un gran fanático de Monty Python's Flying Circus). Guido van Rossum
            
            
            
#             Los objetivos de Python
# En 1999, Guido van Rossum definió sus objetivos para Python:

    # Un lenguaje fácil e intuitivo tan poderoso como los de los principales competidores.
    # De código abierto, para que cualquiera pueda contribuir a su desarrollo.
    # El código que es tan comprensible como el inglés simple.
    # Adecuado para tareas cotidianas, permitiendo tiempos de desarrollo cortos.